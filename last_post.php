        <h3>25.&nbsp;lutego 2024&nbsp;r.</h3>
        <h3>II&nbsp;NIEDZIELA WIELKIEGO POSTU, Rok&nbsp;B</h3>
    </div>
    <div class="col">
        <p>1. Z&nbsp;racji, że&nbsp;w&nbsp;naszym kościele jest Stacyjna Droga
        Krzyżowa, wyjątkowo, w&nbsp;najbliższy piątek Droga Krzyżowa
        o&nbsp;godz.&nbsp;17.00 - zapraszamy do&nbsp;udziału.</p>
        <p>2. <strong>Parafialne Rekolekcje Wielkopostne</strong>
        w&nbsp;naszej parafii rozpoczną się w&nbsp;<strong>piątą niedzielę
        Wielkiego Postu</strong>, tj.&nbsp;17&nbsp;marca br. Parafialna
        Spowiedź Wielkopostna w&nbsp;Uroczystość św.&nbsp;Józefa,
        tj.&nbsp;19&nbsp;marca br.</p>
        <p>3. W&nbsp;każdą niedzielę Wielkiego Postu nabożeństwo „Gorzkich
        Żali” z&nbsp;nauką pasyjną o&nbsp;godz.&nbsp;17.15, następnie Msza
        Święta.</p>
        <p>4. W&nbsp;ramach jałmużny wielkopostnej, dziś po&nbsp;Mszach św.
        zbiórka do&nbsp;puszek na&nbsp;rzecz dzieła pomocy misjonarzom
        „Ad&nbsp;Gentes”.</p>
        <p>5. W&nbsp;tym tygodniu Pierwszy piątek i&nbsp;Pierwsza sobota
        miesiąca. W&nbsp;Pierwszy Piątek Miesiąca, spowiedź w&nbsp;tym dniu
        od&nbsp;godz.&nbsp;17.00.</p>
        <p>6. W&nbsp;najbliższą sobotę o&nbsp;godz.&nbsp;17.15. – nabożeństwo
        Różańcowe poprowadzi Legion Maryi z&nbsp;naszej parafii.</p>
        <p>7. W&nbsp;przyszłą niedzielę w&nbsp;kościele, pół godziny przed
        Mszą&nbsp;św. o&nbsp;godz.&nbsp;11.15 – adoracja Najśw. Sakramentu
        połączona z&nbsp;modlitwą różańcową, którą poprowadzi Koło Żywego
        Różańca z&nbsp;naszej parafii i&nbsp;zmiana tajemnic różańcowych.
        Zapraszamy do&nbsp;udziału w&nbsp;tej modlitwie.</p>
        <p>8. Są&nbsp;jeszcze wolne miejsce w&nbsp;drugim autokarze
        na&nbsp;pielgrzymkę do&nbsp;Włoch. Szczegółowe informacje
        u&nbsp;ks.&nbsp;Krystiana Musiała.</p>
        <p>9. Zachęcamy do&nbsp;czytania prasy katolickiej: „Niedziela”, „Gość
        Niedzielny”.</p>
        <p>10. W&nbsp;zakrystii są&nbsp;do&nbsp;nabycia Baranki wielkanocne
        w&nbsp;cenie 8&nbsp;zł.</p>
        <p>11. Wszystkim uczestniczącym we&nbsp;Mszy&nbsp;św. życzymy
        błogosławionej niedzieli.</p>
