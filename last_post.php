        <h3>7.&nbsp;lipca 2024&nbsp;r.</h3>
        <h3>XIV&nbsp;Niedziela Zwykła, Rok&nbsp;B</h3>
    </div>
    <div class="col">
        <p>1. Przypominamy, że&nbsp;Wyższe Seminarium Duchowne
        w&nbsp;Sandomierzu przyjmuje zgłoszenia nowych kandydatów, którzy
        chcieliby rozpocząć formację i&nbsp;studia przygotowujące
        do&nbsp;sakramentu kapłaństwa. Chętni mogą zgłaszać się osobiście
        w&nbsp;siedzibie Seminarium Duchownego w&nbsp;Sandomierzu,
        ul.&nbsp;Żeromskiego 6. Możliwy jest także kontakt telefoniczny lub
        mailowy.</p>
        <p>2. Na&nbsp;stoliku pod chórem jest najnowsza prasa katolicka.
        Zachęcamy do&nbsp;nabywania i&nbsp;czytania prasy katolickiej.
        Są&nbsp;czasopisma: „Niedziela”, „Miłujcie się”, „Gość Niedzielny”.</p>
        <p>3. W&nbsp;przyszłą niedzielę w&nbsp;czasie Mszy Świętych będzie się
        z&nbsp;nami modlił zespół "Ta&nbsp;Ruta" z&nbsp;Ukrainy
        z&nbsp;Tarnopola. Oni Pomagają sierocińcowi w&nbsp;Tarnopolu
        i&nbsp;ludziom ze&nbsp;wschodu, którzy są&nbsp;szczególnie dotknięci
        wojną. Piszą w&nbsp;mailu: „W&nbsp;sierocińcu przebywają dzieci
        w&nbsp;wieku od&nbsp;jednego miesiąca do&nbsp;18&nbsp;lat i&nbsp;jest
        tych dzieci do&nbsp;150 osób. Prosimy o&nbsp;pomoc, czy to&nbsp;będą
        słodycze dla tych dzieci, lub jedzenie: konserwy, kasza, żywność
        zapakowana o&nbsp;wydłużonym okresie przydatności do&nbsp;spożycia,
        przybory szkolne, pieluchy, chemia. Będziemy mieli płyty
        i&nbsp;pamiątki, ze&nbsp;sprzedaży których pieniądze będą
        na&nbsp;paliwo, na&nbsp;samochód, kupimy coś z&nbsp;wyposażenia
        do&nbsp;domu dziecka.</p>
        <p>4. W&nbsp;dniach od&nbsp;4 do&nbsp;12 sierpnia br. odbędzie się
        41&nbsp;Piesza Pielgrzymka Ziemi Sandomierskiej na&nbsp;Jasną Górę pod
        hasłem <strong>„Rodzina Zakorzeniona w&nbsp;Chrystusie
        i&nbsp;Kościele”</strong>. Więcej szczegółowych informacji na&nbsp;ten
        temat w&nbsp;gablocie parafialnej.</p>
        <p>5. Ks.&nbsp;Biskup Ordynariusz zamianował ks.&nbsp;Wojciecha Zająca
        wikariuszem w&nbsp;naszej parafii. Ks.&nbsp;Wojciech uprzednio
        pracował w&nbsp;parafii św.&nbsp;Teresy od&nbsp;Dzieciątka Jezus
        w&nbsp;Godziszowie koło Janowa Lubelskiego. Życzymy nowemu
        Ks.&nbsp;Wikariuszowi wszystkiego dobrego w&nbsp;posługiwaniu
        w&nbsp;naszej parafii.</p>
        <p>6. Wszystkim, którzy w&nbsp;tym tygodniu obchodzą swoje imieniny,
        urodziny, inne rocznice, składamy najlepsze życzenia. Dziękujemy
        za&nbsp;modlitwę w&nbsp;intencji nas księży i&nbsp;za&nbsp;ofiary
        na&nbsp;rzecz parafii. Wszystkim uczestniczącym we&nbsp;Mszy&nbsp;św.
        życzymy błogosławionej niedzieli.</p>
