        <h3>31.&nbsp;grudnia 2023&nbsp;r.</h3>
        <h3>NIEDZIELA – Święto Świętej Rodziny, Rok&nbsp;B</h3>
    </div>
    <div class="col">
        <p>1. Dziękujemy wszystkim tym rodzinom z&nbsp;naszej parafii,
        z&nbsp;Chwałek, Milczan i&nbsp;z&nbsp;Kobiernik, które w&nbsp;minionym
        tygodniu przyjęły nas kapłanów z&nbsp;wizytą duszpasterską. Niech
        to&nbsp;błogosławieństwo Boże, które pozostawiliśmy w&nbsp;Waszych
        domach, napełni Was i&nbsp;wasze rodziny pokojem i&nbsp;radością,
        umocni wiarę i&nbsp;pomaga z&nbsp;ufnością pokładaną w&nbsp;Bogu
        przezwyciężać różne problemy codziennego życia. Serdeczne: <em>Bóg
        zapłać</em>.</p>
        <p>2. Jutro - Uroczystość Bożej Rodzicielki – Nowy Rok. Światowy
        Dzień Pokoju.</p>
        <p>3. W&nbsp;najbliższą sobotę – Uroczystość Objawienia Pańskiego –
        w&nbsp;tradycji: Święto Trzech Króli – prządek Mszy&nbsp;św., jak
        w&nbsp;niedzielę. Poświęcenie kredy i&nbsp;kadzidła na&nbsp;Mszach św.
        Poświęconą kredą oznaczamy odrzwia naszych domów i&nbsp;mieszkań
        <strong>C+M+B 2023</strong>. O&nbsp;godz.&nbsp;12.00 w&nbsp;tym dniu
        wyruszy od&nbsp;naszego kościoła orszak z&nbsp;jednym z&nbsp;„Trzech
        Króli”. Wspólnie udamy się ze&nbsp;śpiewem kolęd do&nbsp;stajenki
        na&nbsp;rynku Starego Miasta, aby złożyć pokłon Nowonarodzonemu
        Królowi Świata. Zakończenie uroczystości o&nbsp;godz.&nbsp;13.30.
        Będzie gorąca herbata i&nbsp;pączek. Będą papierowe korony dla
        uczestników orszaku. Zapraszamy wszystkich, dorosłych, młodzież
        i&nbsp;dzieci!</p>
        <p>4. Jutro, w&nbsp;kościele, pół godziny przed Mszą&nbsp;św., która
        sprawowana jest o&nbsp;godz.&nbsp;11.15 – adoracja Najśw. Sakramentu
        połączona z&nbsp;modlitwą różańcową, którą poprowadzi Koło Żywego
        Różańca z&nbsp;naszej parafii i&nbsp;zmiana tajemnic różańcowych.</p>
