        <h3>26.&nbsp;maja 2024&nbsp;r.</h3>
        <h3>Uroczystość Najświętszej Trójcy, Rok&nbsp;B</h3>
    </div>
    <div class="col">
        <p>1. Dziś obchodzimy Dzień Matki, jest okazja do&nbsp;wyrażenia
        wdzięczności naszym Mamom, poprzez modlitwę w&nbsp;ich intencji
        i&nbsp;życzenia oraz upominki.</p>
        <p>2. Próby dla dzieci przeżywających w&nbsp;tym roku 1&nbsp;rocznicę
        Komunii Świętej w&nbsp;najbliższy poniedziałek i&nbsp;środę
        o&nbsp;godz.&nbsp;18.30.</p>
        <p>3. Uroczystość Najświętszego Ciała i&nbsp;Krwi Pańskiej
        30&nbsp;maja br. w&nbsp;najbliższy czwartek. Rozpoczęcie, tradycyjnie
        Mszą Świętą o&nbsp;godzinie 9.30 w&nbsp;naszym kościele parafialnym,
        pod przewodnictwem Księdza Biskupa Krzysztofa Nitkiewicza,
        z&nbsp;udziałem kapłanów z&nbsp;miasta Sandomierza. Następnie procesja
        Eucharystyczna ul.&nbsp;Mickiewicza do&nbsp;4&nbsp;ołtarzy.
        Zakończenie i&nbsp;rozwiązanie procesji przy Wyższym Seminarium
        Duchownym. Nasza parafia przygotowuje pierwszy ołtarz na&nbsp;trasie
        procesji. Zapraszamy katolickie organizacje i&nbsp;wspólnoty,
        wszystkich do&nbsp;wspólnej modlitwy. Dzieci w&nbsp;białych strojach
        zapraszamy do&nbsp;sypania kwiatów.</p>
        <p>4. Zapraszamy na&nbsp;Męski Różaniec ulicami naszego miasta
        w&nbsp;każdą pierwszą sobotę miesiąca. Rozpoczęcie Mszą Świętą
        o&nbsp;godz.&nbsp;17.30 w&nbsp;Bazylice Katedralnej, następnie
        15&nbsp;min adoracja, po&nbsp;której nastąpi różaniec w&nbsp;drodze
        pod pomnik Jana Pawła&nbsp;II na&nbsp;błoniach.</p>
        <p>5. W&nbsp;czwartek 30&nbsp;maja br. o&nbsp;godz.&nbsp;19.00
        odbędzie się tradycyjna "Majówka na&nbsp;statku", pod patronatem
        ks.&nbsp;Bpa Krzysztofa Nitkiewicza, na&nbsp;którą bardzo serdecznie
        zapraszamy. Chętnych prosimy o&nbsp;odebranie bezpłatnych biletów
        w&nbsp;zakrystii.</p>
        <p>6. Apostolat Margaretka serdecznie zaprasza wszystkich członków
        apostolatu, jak również ludzi pragnących wspólnie się pomodlić
        w&nbsp;intencji kapłanów na&nbsp;II&nbsp;Diecezjalne Spotkanie
        Apostolatu Margaretka, które odbędzie się w&nbsp;naszej parafii
        1&nbsp;czerwca. Konferencję wygłosi Ks.&nbsp;Bogusław Kowalski –
        przyjaciel śp.&nbsp;Ks.&nbsp;Piotra Pawlukiewicza, który razem
        z&nbsp;nim napisał książkę <em>„Czarny humor, czyli o&nbsp;Kościele
        na&nbsp;wesoło”</em>. Szczegółowy program na&nbsp;plakacie
        w&nbsp;gablocie.</p>
        <p>7. W&nbsp;najbliższą sobotę (1&nbsp;czerwca) – I&nbsp;sobota
        miesiąca, o&nbsp;godz.&nbsp;17.15. – nabożeństwo Różańcowe. Szczególną
        modlitwą w&nbsp;tym dniu chcemy objąć wszystkie dzieci, zapraszamy
        rodziców do&nbsp;wspólnej modlitwy.</p>
        <p>8. W&nbsp;przyszłą niedzielę w&nbsp;kościele, pół godziny przed
        Mszą&nbsp;św. o&nbsp;godz.&nbsp;11.15 – adoracja Najśw. Sakramentu
        połączona z&nbsp;modlitwą różańcową, którą poprowadzi Koło Żywego
        Różańca z&nbsp;naszej parafii i&nbsp;zmiana tajemnic różańcowych.</p>
        <p>9. W&nbsp;sobotę – 8&nbsp;czerwca w&nbsp;Stalowej Woli odbędzie się
        Diecezjalny Dzień Młodych, początek o&nbsp;godz.&nbsp;11.00. Zachęcamy
        młodzież do&nbsp;wzięcia udziału w&nbsp;tym wspólnym spotkaniu młodych
        z&nbsp;diecezji. Szczegółowe informacje i&nbsp;zapisy u&nbsp;Księży
        Wikariuszy, do&nbsp;30&nbsp;maja br.</p>
        <p>10. Informujemy również, że&nbsp;8&nbsp;czerwca z&nbsp;Sandomierza
        do&nbsp;Rozwadowa przez Sokolniki, Orliska, Kotową Wolę przejedzie
        XV&nbsp;Rajd Papieski. Zgłoszenia wraz z&nbsp;oświadczeniami należy
        składać osobiście w&nbsp;terminie od&nbsp;27&nbsp;maja 2024&nbsp;r.
        do&nbsp;7&nbsp;czerwca 2024&nbsp;r. (w&nbsp;godzinach 8:00-12:00)
        w&nbsp;MOSiR Miejski Stadion Sportowy, ul.&nbsp;Koseły&nbsp;3a lub
        elektronicznie e-mail:
        <a href="mailto:sekretariat@mosir.sandomierz.pl">sekretariat@mosir.sandomierz.pl</a>
        od&nbsp;3&nbsp;czerwca 2024&nbsp;r. lub przed startem w&nbsp;biurze
        organizacyjnym rajdu na&nbsp;Bulwarze Marszałka J.&nbsp;Piłsudskiego
        w&nbsp;Sandomierzu.</p>
        <p>11. W&nbsp;trwającym w&nbsp;naszej diecezji Roku Rodziny,
        centralnym wydarzeniem będzie II&nbsp;Kongres Rodzin, zaplanowany
        na&nbsp;15&nbsp;czerwca br. w&nbsp;Sandomierzu. Tego dnia będziemy
        świętować 25&nbsp;rocznicę pobytu św.&nbsp;Jana Pawła&nbsp;II
        na&nbsp;ziemi Sandomierskiej. Chcąc również podkreślić znaczenie
        rodziny i&nbsp;małżeństwa, w&nbsp;trakcie uroczystości będą wręczane
        dyplomy małżonkom przeżywającym 25&nbsp;lub 50&nbsp;rocznicę ślubu.
        Prosimy, aby ci&nbsp;małżonkowie jubilaci z&nbsp;naszej parafii, chcący
        otrzymać taki dyplom z&nbsp;błogosławieństwem biskupim, zgłosili takie
        pragnienie w&nbsp;zakrystii, dziś po&nbsp;Mszy&nbsp;św. lub
        w&nbsp;kolejne dni do&nbsp;Uroczystości Bożego Ciała.</p>
        <p>12. Zachęcamy do&nbsp;nabywania i&nbsp;czytania prasy katolickiej.
        Gazety są&nbsp;na&nbsp;stoliku pod chórem.</p>
        <p>13. Nabożeństwo czerwcowe bezpośrednio po&nbsp;Mszy&nbsp;św.
        wieczornej przez wszystkie dni miesiąca czerwca.</p>
        <p>14. Dziękujemy parafianom za&nbsp;modlitwę, wszelką pomoc, składane
        ofiary na&nbsp;potrzeby parafii. Wszystkim życzymy błogosławionej
        Niedzieli.</p>
