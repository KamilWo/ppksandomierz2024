        <li><strong>Schola dziecięca</strong> – piątek
        o&nbsp;godz.&nbsp;16.30<br></li>
        </ul>
        <p>Zapraszamy także nowe osoby, które chciałyby włączyć się
        do&nbsp;grup duszpasterskich.</p>
