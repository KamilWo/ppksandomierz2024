        <h3>15.&nbsp;grudnia 2024&nbsp;r.</h3>
        <h3>III&nbsp;NIEDZIELA ADWENTU, Rok&nbsp;C</h3>
    </div>
    <div class="col">
        <p>1. Zachęcamy parafian do&nbsp;uczestnictwa we&nbsp;Mszach św.
        Roratach i&nbsp;do&nbsp;spotkania z&nbsp;przebaczającym Jezusem
        w&nbsp;Sakramencie Pokuty – <strong>Spowiedź&nbsp;św. adwentowa
        w&nbsp;naszej parafii będzie w&nbsp;najbliższy czwartek w&nbsp;godzinach
        od&nbsp;10.00 do&nbsp;11.00 oraz od&nbsp;16.30 do&nbsp;18.00.</strong>
        Msza&nbsp;św. Roraty w&nbsp;naszym kościele, w&nbsp;dni powszednie,
        w&nbsp;okresie Adwentu o&nbsp;godz.&nbsp;6.30. Dokładny program
        spowiedzi adwentowej w&nbsp;sandomierskich parafiach
        w&nbsp;parafialnej gablocie.</p>
        <p>2. Można nabyć świece wigilijną „Caritas” – Wigilijne Dzieło Pomocy
        Dzieciom - jak to&nbsp;było w&nbsp;poprzednich latach. Mała świeca -
        15&nbsp;zł, duża - 25&nbsp;zł. Świece są&nbsp;w&nbsp;zakrystii.
        Poświęcone Opłatki wigilijne też można nabyć w&nbsp;zakrystii.
        Zapraszamy.</p>
        <p>3. W&nbsp;zeszłą niedzielę po&nbsp;Mszach św. do&nbsp;puszek
        na&nbsp;wsparcie Kościoła na&nbsp;Wschodzie zostało zebrane
        2936&nbsp;zł. <em>Bóg zapłać</em> wszystkim ofiarodawcom.</p>
        <p>4. W&nbsp;najbliższą sobotę tj.&nbsp;21&nbsp;grudnia br.
        od&nbsp;godz.&nbsp;8.30 odwiedzimy tych chorych z&nbsp;sakramentalną
        posługą, którzy zostaną zgłoszeni w&nbsp;zakrystii lub
        w&nbsp;parafialnej kancelarii.</p>
        <p>5. Zapraszamy wszystkie dzieci z&nbsp;naszej parafii, szczególnie
        przygotowujące się do&nbsp;Pierwszej Komunii Świętej i&nbsp;Rocznicowe
        na&nbsp;przedświąteczne spotkanie do&nbsp;salki parafialnej
        w&nbsp;najbliższą sobotę, 21&nbsp;grudnia o&nbsp;godz.&nbsp;10:00.
        Na&nbsp;spotkaniu dzieci będą przygotowywać najdłuższy łańcuch
        choinkowy, którym zostanie udekorowana choinka w&nbsp;naszym kościele.
        Prosimy by&nbsp;na&nbsp;spotkanie dzieci wzięły ze&nbsp;sobą klej.
        A&nbsp;w&nbsp;następną niedzielę, 22&nbsp;grudnia zachęcamy
        by&nbsp;na&nbsp;mszę świętą o&nbsp;12:30 dzieci przyniosły
        ze&nbsp;sobą po&nbsp;jednej ozdobie choinkowej. Po&nbsp;Mszy Świętej
        dla dzieci będzie wspólne ubieranie choinki w&nbsp;naszym kościele.</p>
        <p>6. W&nbsp;przyszła niedzielę na&nbsp;Mszy Świętej
        o&nbsp;godz.&nbsp;12.30 harcerze przyniosą „Betlejemskie Światełko
        Pokoju”. Światełko będzie można zabrać do&nbsp;domu od&nbsp;niedzieli
        do&nbsp;wtorku w&nbsp;godzinach kiedy kościół jest otwarty. Osoby,
        które chcą zabrać światełko do&nbsp;domu, prosimy by&nbsp;przyniosły
        ze&nbsp;sobą znicz.</p>
        <p>7. Zachęcamy do&nbsp;czytania prasy katolickiej; „Gość Niedzielny”,
        „Niedziela”, są&nbsp;do&nbsp;nabycia na&nbsp;stoliku pod chórem
        w&nbsp;kościele.</p>
        <p>8. Dziękujemy za&nbsp;modlitwę w&nbsp;intencji kapłanów,
        za&nbsp;wszelką pomoc, złożone ofiary na&nbsp;rzecz parafii. Wszystkim
        uczestniczącym we&nbsp;Mszy&nbsp;św. życzymy błogosławionej
        niedzieli.</p>
