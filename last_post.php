        <h3>16.&nbsp;czerwca 2024&nbsp;r.</h3>
        <h3>XI&nbsp;Niedziela Zwykła, Rok&nbsp;B</h3>
    </div>
    <div class="col">
        <p>1. Uroczysta Msza&nbsp;św. odpustowa ku&nbsp;czci św.&nbsp;Brata
        Alberta Chmielowskiego w&nbsp;najbliższy poniedziałek
        o&nbsp;godz.&nbsp;18.00, celebrował ją&nbsp;będzie ks.&nbsp;Sebastian
        Mroczkowski, który też wygłosi okolicznościową homilię. Zapraszamy.
        Msze&nbsp;św. w&nbsp;tym dniu o&nbsp;6.30, 9.00
        i&nbsp;o&nbsp;18.00.</p>
        <p>2. W&nbsp;najbliższy wtorek, jak w&nbsp;każdy trzeci wtorek
        miesiąca w&nbsp;tym roku, zapraszamy na&nbsp;kolejne spotkanie
        Akademii Biblijnej w&nbsp;Sandomierzu; poprowadzi je&nbsp;wykładowca
        Pisma świętego w&nbsp;naszym seminarium, ks.&nbsp;Paweł Lasek.
        Tematem tego spotkania będzie: <strong>Naród wybrany - kto dziś jest
        narodem wybranym?</strong> Spotkanie będzie we&nbsp;wtorek,
        18&nbsp;czerwca o&nbsp;godz.&nbsp;18.45, w&nbsp;auli Wyższego
        Seminarium Duchownego. W&nbsp;spotkaniu będzie można uczestniczyć
        także internetowo, będzie ono transmitowane przez Facebooka,
        na&nbsp;profilu <em>WSDSandomierz</em>. Serdecznie zapraszamy!</p>
        <p>3. W&nbsp;najbliższą środę w&nbsp;kościele św.&nbsp;Józefa
        w&nbsp;Sandomierzu o&nbsp;godz.&nbsp;18.00 – Msza&nbsp;św.
        w&nbsp;intencji mężczyzn, a&nbsp;o&nbsp;18.30 - spotkanie
        formacyjne.</p>
        <p>4. W&nbsp;piątek o&nbsp;godz.&nbsp;8.00, na&nbsp;zakończenie roku
        szkolnego, odprawimy Mszę Świętą dziękczynną za&nbsp;otrzymane łaski
        w&nbsp;ciągu całego roku. Zapraszamy dzieci, młodzież, rodziców,
        nauczycieli do&nbsp;udziału we&nbsp;Mszy Świętej. Okazja
        do&nbsp;spowiedzi codziennie wieczorem przed Mszą Świętą.</p>
        <p>5. Wszystkim udającym się na&nbsp;autokarową pielgrzymkę
        do&nbsp;Włoch przypominamy, że&nbsp;wyjazd jest w&nbsp;nocy z&nbsp;21
        na&nbsp;22&nbsp;czerwca (piątek/sobota) o&nbsp;godz.&nbsp;1.00
        z&nbsp;parkingu przy kościele. Przypominamy o&nbsp;zabraniu
        80&nbsp;Euro, które należy przekazać pilotowi. Osoby jadące w&nbsp;1
        i&nbsp;w&nbsp;2&nbsp;autokarze wsiadają w&nbsp;Sandomierzu.</p>
        <p>6. Dnia 22&nbsp;czerwca (sobota) w&nbsp;Domu Katolickim
        w&nbsp;Sandomierzu o&nbsp;godz.&nbsp;15.00, odbędzie się wręczenie
        Diecezjalnej Nagrody Arbor bona, połączone z&nbsp;koncertem Moniki
        Kuszyńskiej. Wstęp na&nbsp;koncert podobnie, jak w&nbsp;roku ubiegłym
        jest bezpłatny. Zapraszamy wiernych na&nbsp;to&nbsp;wydarzenie.</p>
        <p>7. W&nbsp;przyszłą niedzielę, tj.&nbsp;23&nbsp;czerwca obchodzimy
        Dzień Ojca. W&nbsp;naszej modlitwie polecajmy Panu Bogu wszystkich
        ojców.</p>
        <p>8. Wszystkim uczestniczącym we&nbsp;Mszy&nbsp;św. życzymy
        błogosławionej niedzieli.</p>
