    <div class="col">
        <p>1. W&nbsp;sobotę 14&nbsp;września przeżywać będziemy doroczny
        odpust parafialny. Msze&nbsp;św. w&nbsp;tym dniu sprawowane będą
        o&nbsp;godz.&nbsp;6.30, 9.00 i&nbsp;18.00 – <strong>uroczysta
        Msza&nbsp;św. odpustowa z&nbsp;udziałem kapłanów
        z&nbsp;dekanatu.</strong> Uroczystą Mszę&nbsp;Św. odpustową poprzedzi
        o&nbsp;godz.&nbsp;17.15, występ <strong>Chóru i&nbsp;Orkiestry
        kameralnej – Jubileum – z&nbsp;parafii p.w.&nbsp;Wieczerzy Pańskiej
        z&nbsp;Lublina</strong>. Zapraszamy parafian i&nbsp;gości
        do&nbsp;udziału w&nbsp;uroczystościach odpustowych już
        od&nbsp;wspomnianej godz.&nbsp;17.15.</p>
        <p>2. Dziękujemy parafianom, Panom: Wacławowi Miszczakowi
        i&nbsp;Leszkowi Czajce za&nbsp;wykonanie nowego daszku nad studnią
        przed kościołem parafialnym i&nbsp;renowację pokrywy na&nbsp;studni.
        Poprzednia konstrukcja spróchniała. Trzeba było zrobić zdecydowanie
        trwalszą i&nbsp;ładniejszą konstrukcję. Bardzo dziękujemy. <em>Bóg
        zapłać</em>.</p>
        <p>3. Dnia 14&nbsp;września w&nbsp;kościele seminaryjnym
        pw.&nbsp;św.&nbsp;Michała Archanioła o&nbsp;godz.&nbsp;9.00 odbędzie
        się Msza&nbsp;św. w&nbsp;intencji kobiet sprawowana przez Biskupa
        Sandomierskiego Krzysztofa Nitkiewicza oraz bp.&nbsp;Wisława
        Szlachetkę, odpowiedzialnego za&nbsp;duszpasterstwo kobiet.<br>
        Szczegóły w&nbsp;załączniku w&nbsp;gablocie.</p>
        <p>4. W&nbsp;piątek - 13&nbsp;września w&nbsp;Klasztorze Braci
        Mniejszych Kapucynów w&nbsp;Rozwadowie odbędzie się spotkanie
        trzeźwiejących alkoholików. Początek o&nbsp;godz.&nbsp;15.00.
        Zachęcamy do&nbsp;udziału w&nbsp;nim osoby zainteresowane
        i&nbsp;chętne do&nbsp;współpracy w&nbsp;tego rodzaju
        duszpasterstwie.</p>
        <p>5. W&nbsp;Parafii Św.&nbsp;Stanisława BM w&nbsp;Ostrowcu
        Św./Denkowie odbędą się rekolekcje pt.&nbsp;„W&nbsp;ciemno
        ze&nbsp;światłem, czyli jak nie stracić wiary", organizowane przez
        Parafię Św.&nbsp;Stanisława i&nbsp;Domowy Kościół dla rodzin,
        i&nbsp;młodzieży z&nbsp;okazji Roku Rodziny. Rekolekcje poprowadzi
        o.&nbsp;Tomasz Nowak OP - duszpasterz w&nbsp;Lednicy. Rekolekcje
        odbędą się w&nbsp;dniach 10-12&nbsp;września br. godz.&nbsp;18.00.
        W&nbsp;dniach 11-12&nbsp;września na&nbsp;godz.&nbsp;16.00
        w&nbsp;sposób szczególny zaproszona jest młodzież.</p>
        <p>6. Zachęcamy do&nbsp;nabywania i&nbsp;czytania prasy katolickiej.
        Są&nbsp;czasopisma „Niedziela”, „Miłujcie się”, „Gość Niedzielny”
        i&nbsp;„Mały Gość Niedzielny”.</p>
        <p>7. Od&nbsp;tego tygodnia Koronka do&nbsp;Bożego Miłosierdzia będzie
        odmawiana w&nbsp;piątki o&nbsp;godz.&nbsp;17.45. Nowenna do&nbsp;MBNP
        w&nbsp;środę po&nbsp;Mszy świętej wieczorowej.</p>
        <p>8. Przypominamy, że&nbsp;z&nbsp;Sakramentu spowiedzi w&nbsp;naszej
        parafii można skorzystać w&nbsp;dni powszednie 30&nbsp;minut przed
        Mszą Świętą, w&nbsp;niedziele i&nbsp;święta podczas Mszy Świętej.</p>
        <p>9. Zapraszamy na&nbsp;spotkania duszpasterskie dla dzieci
        i&nbsp;młodzieży przygotowujących się do&nbsp;przyjęcia sakramentu
        bierzmowania, pierwszej komunii świętej, oraz przeżywających rocznicę
        komunii świętej w&nbsp;naszej parafii.<br>
        <strong>Dzieci pierwszokomunijne</strong> z&nbsp;klas trzecich
        z&nbsp;rodzicami oraz <strong>dzieci rocznicowe</strong> z&nbsp;klas
        czwartych z&nbsp;rodzicami zapraszamy na&nbsp;obowiązkowe spotkanie
        w&nbsp;<strong>niedzielę 15&nbsp;września</strong>. O&nbsp;godzinie
        12:30 Msza święta z&nbsp;kazaniem dla dzieci, a&nbsp;po&nbsp;Mszy
        świętej spotkanie organizacyjne w&nbsp;kościele.<br>
        <strong>Kandydatów do&nbsp;bierzmowania</strong> z&nbsp;klas: 6, 7
        i&nbsp;8&nbsp;oraz ich rodziców zapraszamy na&nbsp;obowiązkowe
        spotkanie we&nbsp;wspomnienie Świętego Stanisława Kostki, patrona
        młodzieży we&nbsp;<strong>środę 18&nbsp;września</strong>.
        O&nbsp;godzinie 18:00 Msza święta i&nbsp;po&nbsp;Mszy świętej
        spotkanie w&nbsp;kościele. Pragniemy przypomnieć, że&nbsp;według
        „Wskazań Episkopatu Polski dotyczących przygotowania
        do&nbsp;sakramentu bierzmowania”, w&nbsp;naszej parafii przygotowanie
        młodzieży do&nbsp;sakramentu bierzmowania odbywa się przez 3&nbsp;lata
        (w&nbsp;6, 7 i&nbsp;8&nbsp;klasie szkoły podstawowej). Sakrament ten
        jest udzielany przy końcu 8&nbsp;klasy.</p>
        <p>10. <strong>Spotkania grup duszpasterskich:</strong></p>
        <ul>
        <li><strong>Liturgiczna Służba Ołtarza</strong> (ministranci
        i&nbsp;lektorzy) w&nbsp;piątek – 13&nbsp;września po&nbsp;Mszy świętej
        wieczorowej. Prosimy, aby na&nbsp;to&nbsp;spotkanie przyszli wszyscy
        ministranci. Zapraszamy także nowych chłopców, którzy chcieliby
        dołączyć do&nbsp;grona LSO.</li>
        <li><strong>Legion Maryi</strong> – środa po&nbsp;Mszy świętej
